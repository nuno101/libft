char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (*s != '\0')
	{
		*s++;
		i++;
	}
	if (c == '\0')
		return (*s);
	while (i >= 0)
	{
		if (s == c)
			return (*s);
		else
			i--;
	}
	return ('\0');
}
